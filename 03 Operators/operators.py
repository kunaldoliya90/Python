a = 10
b = 4
print(a & b)    # Bitwise AND: 1010 & 0100 = 0000 = 0
print(a | b)    # Bitwise OR: 1010 | 0100 = 1110 = 14
print(~a)       # Bitwise NOT: ~1010 = -(1011) = -11 (Note: This is due to two's complement representation)
print(a ^ b)    # Bitwise XOR: 1010 ^ 0100 = 1110 = 14
print(a >> 2)   # Bitwise right shift: 1010 >> 2 = 0010 = 2
print(a << 2)   # Bitwise left shift: 1010 << 2 = 101000 = 40
